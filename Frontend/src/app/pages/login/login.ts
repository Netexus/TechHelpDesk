import { Component } from '@angular/core';
import { CommonModule } from '@angular/common';
import { FormsModule } from '@angular/forms';
import { AuthService } from '../../services/auth';
import { Router } from '@angular/router';
import { environment } from '../../../enviroments/environment';

@Component({
  selector: 'app-login',
  standalone: true,
  imports: [CommonModule, FormsModule],
  templateUrl: './login.html',
  styleUrls: ['./login.css']
})
export class LoginComponent {
  email = '';
  password = '';
  error = '';
  loading = false;

  // Expose environment URLs for template
  apiDocsUrl = `${environment.apiUrl}/api`;
  coverageUrl = `${environment.apiUrl}/coverage/`;

  constructor(private authService: AuthService, private router: Router) {
    console.log('LoginComponent: Initialized');
  }

  login() {
    this.loading = true;
    this.error = '';
    this.authService.login({ email: this.email, password: this.password }).subscribe({
      next: () => {
        this.loading = false;
        this.router.navigate(['/dashboard']);
      },
      error: (err) => {
        this.error = 'Invalid credentials';
        this.loading = false;
      }
    });
  }

  openTestReports() {
    const message = `Test Coverage Reports:

Backend Tests:
${this.coverageUrl}

API Documentation (Swagger):
${this.apiDocsUrl}

Note: Backend tests report shows the HTML coverage report generated by Jest.
Swagger provides interactive API documentation.`;

    alert(message);

    // Open backend coverage in new tab
    window.open(this.coverageUrl, '_blank');
  }
}
